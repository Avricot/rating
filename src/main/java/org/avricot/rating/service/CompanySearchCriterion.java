package org.avricot.rating.service;

public class CompanySearchCriterion {
    private String companyName;
    private String companyBusinessId;

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(final String companyName) {
        this.companyName = companyName;
    }

    public String getCompanyBusinessId() {
        return companyBusinessId;
    }

    public void setCompanyBusinessId(final String companyBusinessId) {
        this.companyBusinessId = companyBusinessId;
    }
}
